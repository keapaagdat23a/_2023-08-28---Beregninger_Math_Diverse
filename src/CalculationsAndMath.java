public class CalculationsAndMath {
  public static void main(String[] args) {
    int num = 13;
    System.out.println("Result: " + num / 3);
    System.out.println("Remain: " + num % 3);

    System.out.println("Even or odd: " + (num % 2));

    int num1 = 5;
    int num2 = 3;
    int num3 = 4;

    System.out.println(num3 * (num1 + num2));

    System.out.println(2 * -3);
  }
}

